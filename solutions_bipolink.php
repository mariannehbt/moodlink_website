                <p>
                    Bipolink est une solution numérique de suivi pour les patients atteints de troubles de l'humeur.
                    <br>
                    <br>
                    L'utilisateur pourra suivre sa pathologie via certains paramètres comme le sommeil, l'humeur et l'activité sociale.
                    <br>
                    Il sera également alerté en cas de risque de crise.
                    <br>
                    Pour fonctionner, l'application va d'une part collecter des données, et d'autre part, permettre à l'utilisateur de renseigner des informations sur son état de santé ainsi que des évènements de sa journée par exemple.
                    Toutes ces données, totalement sécurisées, n'appartiendront qu'à l'utilisateur, qui en aura le plein contrôle.
                    <br>
                    <br>
                    Enfin, si l'utilisateur le souhaite, il pourra partager ces informations avec ses soignants (médecins et psychologues) pour les aider dans leur pratique, et ce, de façon totalement sécurisée.
