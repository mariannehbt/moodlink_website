                        Les troubles dépressifs
                    </h4>
                    <p class="mt-sm-4 mt-3">
                        Le trouble dépressif est une maladie se manifestant par la survenue d'épisodes dépressifs caractérisés. Ceux-ci peuvent être d'intensités différentes (légère, modérée ou sévère). Ces épisodes se traduisent par une humeur dépressive (tristesse, irritabilité, sensation de vide), une perte inhabituelle d’intérêt ou de plaisir et une baisse de l’énergie. Ces éléments sont accompagnés par d'autres symptômes cognitifs et comportementaux, comme par exemple des troubles de l'appétit, troubles du sommeil, agitation ou ralentissement psychomoteur, fatigue, sentiment de dévalorisation ou de culpabilité, difficultés de concentration, et pensées de mort ou de suicide.
                    </p>
                    <p class="mt-sm-4 mt-3">
                        La perturbation de l’humeur est suffisamment grave pour entrainer une altération marquée du fonctionnement professionnel ou des activités sociales. L’épisode n’est pas imputable aux effets physiologiques d’une substance ou d’une autre maladie.
                    </p>
                    <p class="mt-sm-4 mt-3">
                        Les troubles dépressifs peuvent toucher des personnes de tous âges. En France en 2017, la prévalence annuelle des épisodes dépressifs caractérisés dans la population générale est estimée à 9,8 % chez les 18-75 ans, contre 8,0 % en 2010 (
                        <a href="https://www.santepubliquefrance.fr/etudes-et-enquetes/barometres-de-sante-publique-france/barometre-sante-2017">
                            Baromètre santé 2017
                        </a>
                        ) [3]. Ces chiffres sont donc en augmentation, et les femmes restent deux fois plus touchées que les hommes. 
                    </p>
