                                </h4>
                                <p class="mt-3">
                                    La psychothérapie se fait selon un rythme régulier de séances adaptées aux besoins du patient. Elle peut être effectuée individuellement ou en groupe, et peut être menée par un médecin généraliste ou un psychothérapeute (psychiatre ou psychologue).
                                </p>
                                <p class="mt-3">
                                    Il existe différentes approches psychothérapeutiques recommandées dans les troubles dépressifs [4 ; 5] : la psychothérapie de soutien, les Thérapies Cognitivo-Comportementales (TCC), la psychothérapie d'inspiration analytique et les thérapies systémiques.
