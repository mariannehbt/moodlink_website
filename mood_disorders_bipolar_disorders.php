                        <h4 class="about-left">
                            La prise en charge
                        </h4>
                        <p class="mt-sm-4 mt-3">
                            La phase de diagnostic des troubles bipolaires est très longue et complexe [4]. La durée entre le premier épisode et le diagnostic, puis la mise en place d'un traitement adapté est estimée à 10 ans. Une fois le traitement instauré, la période peut être longue également avant d'arriver à une stabilisation thymique chez le patient.
                        </p>
                        <p class="mt-sm-4 mt-3 mb-5">
                            En plus d’un traitement pharmacologique, la Haute Autorité de Santé (
                            <a href="https://www.has-sante.fr/upload/docs/application/pdf/2009-09/ald_23__lap_troubles_bipolaires_web.pdf">
                                HAS
                            </a>
                            ) [5] recommande la psychothérapie et l’Education Thérapeutique du Patient (ETP), qui permettent d’aider ce dernier à comprendre sa maladie et son évolution.
                        </p>
                        <div class="row offer-grids">
                            <div class="col-lg-6 col-md-12 mt-md-4 mt-4">
