                                    <div class="bg-layer">
                                        <img src="img/image_drugs_2.jpg" class="mb-4" alt="drugs image">
                                        <h4 class="">
                                            Le traitement médicamenteux
                                        </h4>
                                        <p class="mt-3">
                                            Le psychiatre peut prescrire une prise de médicament pour atténuer les phases maniaques et hypomaniaques et lutter contre les phases dépressives. D'après la revue Cochrane [6] et le Journal médical allemand international [7] de 2013, les médicaments les plus efficaces dans la prise en charge de la bipolarité sont les thymorégulateurs (principalement lithium, lamotrigine), et les antipsychotiques (principalement aripiprazole, oxcarbazépine, risperidone, tiagabine).
                                        </p>
                                    </div>
                                </div>
                            </div>
                            <div class="col-lg-6 col-md-12 mt-md-4 mt-4">
                                <div class="ser2">
                                    <div class="bg-layer">
                                        <img src="img/image_psychologue.jpg" class="mb-4" alt="psychologist image">
                                        <h4 class="">
                                            La psychothérapie
                                        </h4>
                                        <p class="mt-3">
                                            Les principales thérapies [8] ayant montré leur efficacité dans traitement des troubles bipolaires sont la thérapie familiale (FFT), la Thérapie Cognitivo-Comportementale (TCC) et thérapie interpersonnelle des rythmes sociaux (IPSRT).
                                            <br>
                                            Les TCC, notamment par la psychoéducation, ont prouvé leur efficacité dans la réduction de la durée des hospitalisations, l’amélioration de la connaissance et de l’observance du traitement, du fonctionnement socioprofessionnel, et de la qualité de vie, ainsi qu'une diminution des rechutes.
                                        </p>
                                    </div>
